package net.mamoe.jpre.event;

/**
 * @author Him188 @ JPRE Project
 */
public enum EventType {
	UNKNOWN(-1),

	MESSAGE_FRIEND(1),
	MESSAGE_GROUP(2),
	MESSAGE_DISCUSSION(3),
	MESSAGE_GROUP_TEMPORARY(4),
	MESSAGE_DISCUSSION_TEMPORARY(5),

	FRIEND_ADD_RESULT(1000),
	FRIEND_ADD_REQUEST(1001),
	FRIEND_STATUS_CHANGE(1002),
	FRIEND_DELETE(1003),
	FRIEND_SIGN_CHANGE(1004),
	FRIEND_TAOTAO_BE_COMMENT(1005),
	FRIEND_TYPING(1006),
	FRIEND_FIRST_CONVERSATION(1007),
	FRIEND_SHAKE(1008),

	GROUP_JOIN_REQUEST(2001),
	GROUP_INVITATION_REQUEST(2002),
	GROUP_INVITATION(2003),
	GROUP_JOIN(2005),

	GROUP_QUIT(2006),
	GROUP_KICK(2007),
	GROUP_DISSOLUTION(2008),
	GROUP_ADMIN_PROMOTION(2009),
	GROUP_ADMIN_DEMOTION(2010),
	GROUP_CARD_CHANGE(2011),
	@Deprecated GROUP_NAME_CHANGE(2012),
	GROUP_NOTIFICATION_CHANGE(2013),

	GROUP_MUTE(2014),
	GROUP_UNMUTE(2015),
	GROUP_WHOLE_MUTE(2016),
	GROUP_WHOLE_UNMUTE(2017),
	GROUP_ANONYMOUS_ENABLE(2018),
	GROUP_ANONYMOUS_DISABLE(2019),

	FRAME_STARTUP(10000),
	FRAME_REBOOT(10001),

	FRAME_QQ_ADD(11000),
	FRAME_QQ_LOGIN(11001),
	FRAME_QQ_OFFLINE(11002),
	FRAME_QQ_FORCE_OFFLINE(11003),
	FRAME_QQ_CRASH(11004),

	JPRE_MENU_ACTION(12003),

	TENPAY_RECEIVE_TRANSFER(80001);

	private final int id;

	EventType(int id) {
		this.id = id;
	}

	public int getId() {
		return id;
	}

	public static EventType match(int id) {
		for (EventType eventType : EventType.values()) {
			if (eventType.getId() == id) {
				return eventType;
			}
		}

		return UNKNOWN;
	}
}


